package loianeAula32;

public class Carro {
	
	private String marca;
	private String modelo;
	private int numPassageiros;
	private Double capCombus;
	private Double conCombus;

	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getNumPassageiros() {
		return numPassageiros;
	}
	public void setNumPassageiros(int numPassageiros) {
		this.numPassageiros = numPassageiros;
	}
	public Double getCapCombus() {
		return capCombus;
	}
	public void setCapCombus(Double capCombus) {
		this.capCombus = capCombus;
	}
	public Double getConCombus() {
		return conCombus;
	}
	public void setConCombus(Double conCombus) {
		this.conCombus = conCombus;
	}
	

}
